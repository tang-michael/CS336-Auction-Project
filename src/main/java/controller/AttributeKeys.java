package controller;

/**
 * Store some important constants.
 */
public class AttributeKeys {
    /**
     * Session attribute key representing the authenticated user.
     */
    public static String AUTHENTICATED_USER = "Principal";

    /**
     * Request parameter key used to denote an error that should be displayed to the user.
     */
    public static String ERROR_REQUEST_PARAM = "error";

    /**
     * Request parameter key used to denote an info message that should be displayed to the user.
     */
    public static String INFO_REQUEST_PARAM = "info";
}
